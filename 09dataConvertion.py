# konversi string ke integer
x = "123"
x_int = int(x)
print(x_int)  # output: 123


# konversi string ke float
x = "3.14"
x_float = float(x)
print(x_float)  # output: 3.14


# konversi integer ke string
x = 42
x_str = str(x)
print(x_str)  # output: "42"


# konversi integer ke boolean
x = 1
x_bool = bool(x)  # angka selain 0 dianggap True
print(x_bool)  # output: True
