# operator matematika

# memberi nilai pada variabel
angka1 = 15
angka2 = 7

# penjumlahan
hasilJumlah = angka1 + angka2
print("hasil penjumlahan adalah =", hasilJumlah)

# pengurangan
hasilPengurangan = angka1 - angka2
print("hasil pengurangan adalah =", hasilPengurangan)

# perkalian
hasilPerkalian = angka1 * angka2
print("hasil perkalian adalah =", hasilPerkalian)

# pembagian
hasilPembagian = angka1 / angka2
print("hasil pembagian adalah =", hasilPembagian)

sisabagi = angka1 % angka2
print("hasil sisaBagi adalah =", sisabagi)

hasilPangkat = angka1 ** 2
print("hasil dari ", angka1, "dipangkatkan 2 adalah =", hasilPangkat)
