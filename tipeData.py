# fungsi dari type() adalah untuk mengetahui dari tipe data yang dieksekusi
# tipe data boolean
print(type(True))
print(type(False))

# tipe data string
print(type("Tipe data string"))
print(type('tipe data string'))

# tipe data integer
print(type(26))

# tipe data float
print(type(26.8))

# tipe data data hexadesimal
print(hex(26))

# tipe data complex
print(type(5j))

# tipe data list
list = ["angga", "anggi", "anggun"]
print(type(list))

# tipe data tuple
tuple = ("angga", "anggi", "anggun")
print(tuple)
print(type(tuple))

# tipe data dictionary
dict = {"nama":"angga", "status":"pengguna"}
print(dict)
print(type(dict))
